<h1 align="center">Groupe C</h1>

<h2>Histoire du Groupe C</h2>

<p>Le Groupe C est la catégorie relative aux voitures de tourisme modifiées, dans le règlement FIA. Cette catégorie a été créée en 1982 pour remplacer l'ancienne catégorie Groupe 2.</br>Le Groupe A regroupe des voitures plus ou moins modifiées par rapport aux voitures de série et doivent être homologuées par la FIA. Afin de modifier les voitures, les constructeurs doivent faire homologuer chaque nouvelle pièce par la FIA, avant de pouvoir les utiliser en course.</p>

