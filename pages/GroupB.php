<h1 align="center">Groupe B</h1>

        <h2>Histoire du Groupe B</h2>

        <p>Le Groupe B est la catégorie relative aux voitures de sport modifiées et produites à 200 exemplaires, dans le règlement de la FIA. Cette catégorie est créée en 1982 pour remplacer les anciennes catégories Groupe 4 et Groupe 5. Cette catégorie est aujourd'hui connu sous le nom de WRC (World Rally Championship).</p>

